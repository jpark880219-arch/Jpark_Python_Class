# 연산자

# 다중커서:alt+마우스클릭
# 다중커서 해제:esc
x=10
y=20
print("x+y=",x+y)
print("x-y=",x-y)
print("x*y=",x*y)
print("x/y=",x/y)
print("x%y=",x%y)


# 정수나누기:몫
